package lista6;

public class Clouds {
    private int all;
    private int dt;

    public Clouds(int all, int dt) {
        this.all = all;
        this.dt = dt;
    }
    public int getAll() {
        return all;
    }

    public void setAll(int all) {
        this.all = all;
    }

    public int getDt() {
        return dt;
    }

    public void setDt(int dt) {
        this.dt = dt;
    }
}
